import { readdirSync, statSync, writeFileSync } from "node:fs";
import { join, relative, sep } from "node:path";

const projectRoot = process.cwd();
const contentRoot = join(projectRoot, "src", "content");
const outputPath = join(projectRoot, "src", "lib", "registry", "generated.ts");

function collectContentFiles(dir) {
  const files = [];

  for (const entry of readdirSync(dir)) {
    const path = join(dir, entry);
    const stat = statSync(path);
    if (stat.isDirectory()) {
      files.push(...collectContentFiles(path));
      continue;
    }
    if (entry.endsWith(".ts") && !entry.endsWith(".test.ts")) {
      files.push(path);
    }
  }

  return files.sort();
}

function toImportPath(file) {
  const fromOutputDir = relative(join(projectRoot, "src", "lib", "registry"), file);
  return fromOutputDir.split(sep).join("/").replace(/\.ts$/, "");
}

const files = collectContentFiles(contentRoot);
const imports = files
  .map((file, index) => `import * as module${index} from "${toImportPath(file)}";`)
  .join("\n");
const modules = files.map((_, index) => `module${index}`).join(", ");

const content = `// This file is generated by scripts/generate-registry.mjs. Do not edit manually.
${imports}
import { RegistryModule } from "./types";

const modules: RegistryModule[] = [${modules}];

export const roomDefinitions = modules.flatMap((module) => module.rooms ?? []);
export const monsterDefinitions = modules.flatMap((module) => module.monsters ?? []);
export const bossDefinitions = modules.flatMap((module) => module.bosses ?? []);
export const equipDefinitions = modules.flatMap((module) => module.equipment ?? []);
export const consumableDefinitions = modules.flatMap((module) => module.consumables ?? []);
export const itemEffectDefinitions = modules.flatMap((module) => module.itemEffects ?? []);

export type RegisteredRoomType = (typeof roomDefinitions)[number]["type"];
`;

writeFileSync(outputPath, content, "utf8");
console.log(`Generated registry from ${files.length} content files.`);
